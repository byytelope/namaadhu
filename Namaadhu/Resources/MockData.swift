private func makeMockPrayerTimes(
  categoryId: Int,
  dayOffset: Int,
  fajrMinutes: Int,
  sunriseMinutes: Int,
  dhuhrMinutes: Int,
  asrMinutes: Int,
  maghribMinutes: Int,
  ishaMinutes: Int
) -> PrayerTimes {
  let calendar = Calendar.current
  let date = calendar.date(
    byAdding: .day,
    value: dayOffset,
    to: calendar.startOfDay(for: .now)
  )!

  return PrayerTimes(
    categoryId: categoryId,
    date: date,
    values: PrayerTimes.Values(
      fajr: fajrMinutes,
      sunrise: sunriseMinutes,
      dhuhr: dhuhrMinutes,
      asr: asrMinutes,
      maghrib: maghribMinutes,
      isha: ishaMinutes
    )
  )
}
